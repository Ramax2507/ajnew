// Function to populate a dropdown
function populateDropdown(dropdownId, items) {
    const dropdown = document.getElementById(dropdownId);
    items.forEach(item => {
        const opt = document.createElement("option");
        opt.value = item;
        opt.textContent = item;
        dropdown.appendChild(opt);
    });
}

// Function to update college dropdown based on selected university
function updateCollegeDropdown(selectedUniversity) {
    const collegeDropdown = document.getElementById("collegeName");
    collegeDropdown.innerHTML = ""; // Clear existing options

    if (options.collegeName[selectedUniversity]) {
        options.collegeName[selectedUniversity].forEach(college => {
            const opt = document.createElement("option");
            opt.value = college;
            opt.textContent = college;
            collegeDropdown.appendChild(opt);
        });
    }
}

// Google Sign-In
const googleSignIn = async () => {
  const provider = new GoogleAuthProvider();
  try {
    const result = await signInWithPopup(auth, provider);
    const user = result.user;

    // Save user data to Firestore
    await setDoc(doc(firestore, "users", user.uid), {
      displayName: user.displayName,
      email: user.email,
      photoURL: user.photoURL,
      lastLogin: new Date().toISOString()
    });

    toggleFormVisibility(true);
    displayUserInfo(user);
  } catch (error) {
    console.error("Error during Google Sign-In:", error);
  }
};

// Google Sign-Out
const googleSignOut = async () => {
  try {
    await signOut(auth);
    toggleFormVisibility(false);
    document.getElementById("userInfo").innerHTML = `<p>Welcome to TNSCHE Survey</p>`;
  } catch (error) {
    console.error("Error during Sign-Out:", error);
  }
};

